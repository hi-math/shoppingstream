/**
 * 로컬 이미지 경로 반환 — Vite publicDir(images/)에서 서빙
 * @param {string|number} id - 상품 ID
 * @returns {string} '/3334217.jpg' 형식
 */
export function localThumb(id) {
  const sid = String(id);
  return `/${sid}.${PNG_IDS.has(sid) ? 'png' : 'jpg'}`;
}
